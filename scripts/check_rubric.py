def generate_traceability(
    rubric_results: list[dict],
    faq_results: list[dict],
    rubric_source: str,
    faq_source: str,
    output_path: Path,
) -> None:
    """Generate or update RUBRIC_TRACEABILITY.md from check results."""
    lines = [
        "# Rubric & FAQ Traceability Matrix",
        "",
        "> **Auto-generated by check_rubric.py**",
        f"> **Rubric source:** `{rubric_source}`",
        f"> **FAQ source:** `{faq_source}`",
        "",
        "---",
        "",
        "## Rubric Items",
        "",
        f"**Source:** `{rubric_source}`",
        "",
        "| # | Category | Requirement | Report Section | Status | Score | Details |",
        "|---|----------|-------------|---------------|--------|-------|---------|",
    ]

    for r in rubric_results:
        req_text = r["text"][:80] + ("..." if len(r["text"]) > 80 else "")
        req_text = req_text.replace("|", "\\|")
        lines.append(
            f"| {r['id']} | {r.get('category', '')} | {req_text} "
            f"| {r['section']} | {r['status']} | {r['score']:.2f} | {r['details']} |"
        )

    lines.extend([
        "",
        "---",
        "",
        "## FAQ Items",
        "",
        f"**Source:** `{faq_source}`",
        "",
        "| # | Question | Report Section | Status | Score | Details |",
        "|---|----------|---------------|--------|-------|---------|",
    ])

    for r in faq_results:
        q_text = r["text"][:80] + ("..." if len(r["text"]) > 80 else "")
        q_text = q_text.replace("|", "\\|")
        lines.append(
            f"| {r['id']} | {q_text} | {r['section']} "
            f"| {r['status']} | {r['score']:.2f} | {r['details']} |"
        )

    # Coverage summary
    all_results = rubric_results + faq_results
    total = len(all_results)
    addressed = sum(1 for r in all_results if r["status"] == "ADDRESSED")
    partial = sum(1 for r in all_results if r["status"] == "PARTIAL")
    gaps = sum(1 for r in all_results if r["status"] == "GAP")
    pct = (addressed / total * 100) if total > 0 else 0

    rubric_total = len(rubric_results)
    rubric_addr = sum(1 for r in rubric_results if r["status"] == "ADDRESSED")
    rubric_pct = (rubric_addr / rubric_total * 100) if rubric_total > 0 else 0

    faq_total = len(faq_results)
    faq_addr = sum(1 for r in faq_results if r["status"] == "ADDRESSED")
    faq_pct = (faq_addr / faq_total * 100) if faq_total > 0 else 0

    lines.extend([
        "",
        "---",
        "",
        "## Coverage Summary",
        "",
        "```",
        f"Rubric:  {rubric_addr}/{rubric_total} ADDRESSED ({rubric_pct:.0f}%)",
        f"FAQ:     {faq_addr}/{faq_total} ADDRESSED ({faq_pct:.0f}%)",
        f"Overall: {addressed}/{total} ADDRESSED ({pct:.0f}%)",
        f"",
        f"PARTIAL items: {partial}",
        f"GAP items:     {gaps}",
        "```",
        "",
    ])

    # List gaps
    gap_items = [r for r in all_results if r["status"] == "GAP"]
    if gap_items:
        lines.append("### GAP Items Requiring Attention")
        lines.append("")
        for r in gap_items:
            critical_tag = " **[CRITICAL]**" if r.get("is_critical") else ""
            lines.append(f"- **{r['id']}**{critical_tag}: {r['text'][:100]}")
        lines.append("")

    # List partials
    partial_items = [r for r in all_results if r["status"] == "PARTIAL"]
    if partial_items:
        lines.append("### PARTIAL Items Requiring Expansion")
        lines.append("")
        for r in partial_items:
            lines.append(f"- **{r['id']}**: {r['text'][:100]} (score: {r['score']:.2f})")
        lines.append("")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines) + "\n")
    print(f"  Traceability written: {output_path}")


