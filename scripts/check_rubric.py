# Patterns for extracting numbered requirements, bullets, or markdown headers
_NUMBERED_RE = re.compile(
    r"^\s*"                   # leading whitespace
    r"(?:"
    r"(\d+)[.)\s]"           # numbered: "1." or "1)" or "1 "
    r"|"
    r"[-*+]\s"               # bullet: "- " or "* " or "+ "
    r"|"
    r"(#{1,4})\s"           # markdown header: "## "
    r")"
    r"\s*(.*)"               # the requirement text
)

_FAQ_Q_RE = re.compile(
    r"^\s*"
    r"(?:"
    r"Q\s*[:.]?\s*"         # Q: or Q. prefix
    r"|"
    r"(?:Question|FAQ)\s*\d*\s*[:.]?\s*"  # Question 1: or FAQ:
    r"|"
    r"(\d+)[.)\s]\s*"      # numbered
    r")"
    r"(.*\?.*)",              # text containing a question mark
    re.IGNORECASE
)


def extract_rubric_items(text: str) -> list[dict]:
    """Extract individual requirement items from rubric source text.

    Returns list of dicts with keys: id, text, category, is_critical.
    """
    items = []
    lines = text.splitlines()
    current_category = "General"
    item_num = 0

    for line in lines:
        stripped = line.strip()
        if not stripped:
            continue

        # Detect category headers (## Category or ALL CAPS lines)
        if stripped.startswith("#"):
            hdr = stripped.lstrip("#").strip()
            if hdr:
                current_category = hdr
            continue
        if stripped.isupper() and len(stripped) > 3 and len(stripped) < 80:
            current_category = stripped.title()
            continue

        m = _NUMBERED_RE.match(line)
        if m:
            num_str = m.group(1)
            header_level = m.group(2)
            text_part = m.group(3) or ""
            text_part = text_part.strip()

            if not text_part or len(text_part) < 5:
                continue

            # Skip if it looks like a section header for a category
            if header_level and not any(
                kw in text_part.lower() for kw in RUBRIC_KEYWORDS
            ):
                current_category = text_part
                continue

            item_num += 1
            item_id = f"R-{item_num:02d}"
            is_critical = any(
                cat.lower() in current_category.lower()
                for cat in CRITICAL_CATEGORIES
            ) or any(
                kw in text_part.lower()
                for kw in ("must", "required", "critical")
            )

            items.append({
                "id": item_id,
                "text": text_part,
                "category": current_category,
                "is_critical": is_critical,
            })

    return items


def extract_faq_items(text: str) -> list[dict]:
    """Extract FAQ questions from FAQ source text.

    Returns list of dicts with keys: id, text.
    """
    items = []
    lines = text.splitlines()
    faq_num = 0

    for line in lines:
        stripped = line.strip()
        if not stripped:
            continue

        m = _FAQ_Q_RE.match(stripped)
        if m:
            # group(1) is the optional number, group(2) is the question text
            q_text = m.group(2) or m.group(0)
            q_text = q_text.strip()
            if len(q_text) < 5:
                continue
            faq_num += 1
            items.append({
                "id": f"FAQ-{faq_num:02d}",
                "text": q_text,
            })
            continue

        # Also catch lines that end with "?" even without Q: prefix
        if stripped.endswith("?") and len(stripped) > 10:
            faq_num += 1
            items.append({
                "id": f"FAQ-{faq_num:02d}",
                "text": stripped,
            })

    return items


