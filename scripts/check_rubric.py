def main():
    parser = argparse.ArgumentParser(
        description="Check rubric/FAQ compliance of a report file.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
    python scripts/check_rubric.py --report-path docs/report.tex
    python scripts/check_rubric.py --report-path docs/report.md --rubric-source spec.md --faq-source faq.md
    python scripts/check_rubric.py --report-path docs/report.tex --traceability RUBRIC_TRACEABILITY.md
        """,
    )
    parser.add_argument(
        "--report-path", required=True,
        help="Path to the report file (.tex or .md)",
    )
    parser.add_argument(
        "--rubric-source", default=DEFAULT_RUBRIC_SOURCE,
        help=f"Path to rubric source file (default: {DEFAULT_RUBRIC_SOURCE})",
    )
    parser.add_argument(
        "--faq-source", default=DEFAULT_FAQ_SOURCE,
        help=f"Path to FAQ source file (default: {DEFAULT_FAQ_SOURCE})",
    )
    parser.add_argument(
        "--traceability", default=None,
        help="Path to filled RUBRIC_TRACEABILITY.md (optional)",
    )
    parser.add_argument(
        "--output", default="rubric_coverage.md",
        help="Path for coverage report output (default: rubric_coverage.md)",
    )

    args = parser.parse_args()

    report_path = Path(args.report_path)
    rubric_source = Path(args.rubric_source)
    faq_source = Path(args.faq_source)
    traceability_path = Path(args.traceability) if args.traceability else None
    output_path = Path(args.output)

    print("Rubric / FAQ Compliance Checker")
    print(f"  Report:       {report_path}")
    print(f"  Rubric:       {rubric_source}")
    print(f"  FAQ:          {faq_source}")
    print(f"  Traceability: {traceability_path or '(none)'}")
    print(f"  Output:       {output_path}")
    print()

    # Run checks
    rubric_results, faq_results, gap_warnings = run_checks(
        report_path, rubric_source, faq_source, traceability_path,
    )

    # Generate/update traceability matrix
    trace_output = traceability_path or Path("RUBRIC_TRACEABILITY.md")
    generate_traceability(
        rubric_results, faq_results,
        str(rubric_source), str(faq_source),
        trace_output,
    )

    # Write coverage report
    write_coverage_report(rubric_results, faq_results, gap_warnings, output_path)

    # Print human-readable summary
    print_coverage_report(rubric_results, faq_results, gap_warnings)

    # Exit code
    all_results = rubric_results + faq_results
    total = len(all_results)
    addressed = sum(1 for r in all_results if r["status"] == "ADDRESSED")
    pct = (addressed / total * 100) if total > 0 else 0
    critical_gaps = sum(
        1 for r in all_results
        if r["status"] == "GAP" and r.get("is_critical")
    )

    if pct >= COVERAGE_THRESHOLD and critical_gaps == 0:
        sys.exit(0)
    else:
        sys.exit(1)


if __name__ == "__main__":
    main()
