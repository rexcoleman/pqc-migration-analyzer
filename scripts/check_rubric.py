def print_coverage_report(
    rubric_results: list[dict],
    faq_results: list[dict],
    gap_warnings: list[str],
) -> None:
    """Print a human-readable coverage report."""
    all_results = rubric_results + faq_results
    total = len(all_results)
    addressed = sum(1 for r in all_results if r["status"] == "ADDRESSED")
    partial = sum(1 for r in all_results if r["status"] == "PARTIAL")
    gaps = sum(1 for r in all_results if r["status"] == "GAP")
    pct = (addressed / total * 100) if total > 0 else 0
    critical_gaps = sum(
        1 for r in all_results
        if r["status"] == "GAP" and r.get("is_critical")
    )

    print()
    print("=" * 70)
    print("RUBRIC / FAQ COMPLIANCE CHECK")
    print("=" * 70)
    print(f"  ADDRESSED: {addressed}")
    print(f"  PARTIAL:   {partial}")
    print(f"  GAP:       {gaps}")
    print(f"  Coverage:  {pct:.0f}% (threshold: {COVERAGE_THRESHOLD}%)")
    if critical_gaps:
        print(f"  CRITICAL GAPS: {critical_gaps}")
    print("-" * 70)

    # Per-item details
    if rubric_results:
        print()
        print("RUBRIC ITEMS:")
        for r in rubric_results:
            marker = "PASS" if r["status"] == "ADDRESSED" else r["status"]
            crit = " [CRITICAL]" if r.get("is_critical") and r["status"] == "GAP" else ""
            text_preview = r["text"][:60] + ("..." if len(r["text"]) > 60 else "")
            print(f"  [{marker}]{crit} {r['id']}: {text_preview}")
            if r["status"] != "ADDRESSED":
                print(f"         {r['details']}")

    if faq_results:
        print()
        print("FAQ ITEMS:")
        for r in faq_results:
            marker = "PASS" if r["status"] == "ADDRESSED" else r["status"]
            text_preview = r["text"][:60] + ("..." if len(r["text"]) > 60 else "")
            print(f"  [{marker}] {r['id']}: {text_preview}")
            if r["status"] != "ADDRESSED":
                print(f"         {r['details']}")

    # Gap pattern warnings
    if gap_warnings:
        print()
        print("COMMON GAP PATTERN WARNINGS:")
        print("  The following patterns were NOT detected in the report:")
        for gw in gap_warnings:
            print(f"  - {gw}")

    print()
    if pct >= COVERAGE_THRESHOLD and critical_gaps == 0:
        print("RESULT: PASS")
    else:
        reasons = []
        if pct < COVERAGE_THRESHOLD:
            reasons.append(f"coverage {pct:.0f}% < {COVERAGE_THRESHOLD}%")
        if critical_gaps > 0:
            reasons.append(f"{critical_gaps} CRITICAL gap(s)")
        print(f"RESULT: FAIL ({', '.join(reasons)})")
    print("=" * 70)


def write_coverage_report(
    rubric_results: list[dict],
    faq_results: list[dict],
    gap_warnings: list[str],
    output_path: Path,
) -> None:
    """Write a Markdown coverage report file."""
    all_results = rubric_results + faq_results
    total = len(all_results)
    addressed = sum(1 for r in all_results if r["status"] == "ADDRESSED")
    partial = sum(1 for r in all_results if r["status"] == "PARTIAL")
    gaps = sum(1 for r in all_results if r["status"] == "GAP")
    pct = (addressed / total * 100) if total > 0 else 0
    critical_gaps = sum(
        1 for r in all_results
        if r["status"] == "GAP" and r.get("is_critical")
    )

    lines = [
        "# Rubric Coverage Report",
        "",
        f"**Coverage:** {addressed}/{total} ADDRESSED ({pct:.0f}%)",
        f"**Threshold:** {COVERAGE_THRESHOLD}%",
        f"**PARTIAL:** {partial} | **GAP:** {gaps} | **CRITICAL GAP:** {critical_gaps}",
        f"**Result:** {'PASS' if pct >= COVERAGE_THRESHOLD and critical_gaps == 0 else 'FAIL'}",
        "",
        "---",
        "",
    ]

    if rubric_results:
        lines.append("## Rubric Items")
        lines.append("")
        lines.append("| # | Status | Requirement | Section | Score |")
        lines.append("|---|--------|-------------|---------|-------|")
        for r in rubric_results:
            req = r["text"][:60].replace("|", "\\|")
            crit = " CRITICAL" if r.get("is_critical") and r["status"] == "GAP" else ""
            lines.append(
                f"| {r['id']} | {r['status']}{crit} | {req} | {r['section']} | {r['score']:.2f} |"
            )
        lines.append("")

    if faq_results:
        lines.append("## FAQ Items")
        lines.append("")
        lines.append("| # | Status | Question | Section | Score |")
        lines.append("|---|--------|----------|---------|-------|")
        for r in faq_results:
            q = r["text"][:60].replace("|", "\\|")
            lines.append(
                f"| {r['id']} | {r['status']} | {q} | {r['section']} | {r['score']:.2f} |"
            )
        lines.append("")

    if gap_warnings:
        lines.append("## Common Gap Pattern Warnings")
        lines.append("")
        lines.append("The following common rubric patterns were NOT detected in the report:")
        lines.append("")
        for gw in gap_warnings:
            lines.append(f"- {gw}")
        lines.append("")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines) + "\n")
    print(f"  Coverage report written: {output_path}")


