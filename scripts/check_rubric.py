def parse_traceability(text: str) -> dict[str, dict]:
    """Parse a filled RUBRIC_TRACEABILITY.md file.

    Returns a dict mapping item_id -> {section, status, evidence}.
    """
    mappings: dict[str, dict] = {}
    lines = text.splitlines()

    for line in lines:
        stripped = line.strip()
        if not stripped.startswith("|"):
            continue

        cells = [c.strip() for c in stripped.split("|")]
        # Filter out empty cells from leading/trailing pipes
        cells = [c for c in cells if c]

        if len(cells) < 4:
            continue

        item_id = cells[0].strip()
        # Skip header rows and separator rows
        if item_id in ("#", "---", "") or item_id.startswith("-"):
            continue
        if not (item_id.startswith("R-") or item_id.startswith("FAQ-") or item_id.startswith("EC-")):
            continue

        # Extract mapped section and status
        # Rubric table: # | Category | Requirement | Report Section | ... | Status
        # FAQ table: # | Question | Report Section | ... | Status
        if item_id.startswith("R-"):
            section = cells[3] if len(cells) > 3 else ""
            status = cells[5] if len(cells) > 5 else ""
            evidence = cells[6] if len(cells) > 6 else ""
        else:
            section = cells[2] if len(cells) > 2 else ""
            status = cells[4] if len(cells) > 4 else ""
            evidence = cells[5] if len(cells) > 5 else ""

        # Clean template placeholders
        for placeholder in ("{{SECTION}}", "{{PARA_OR_LINE}}", "{{ADDRESSED/GAP/PARTIAL}}",
                            "{{QUOTE_OR_REF}}", "{{DATE}}"):
            if section == placeholder:
                section = ""
            if status == placeholder:
                status = ""

        mappings[item_id] = {
            "section": section,
            "status": status.upper() if status else "",
            "evidence": evidence,
        }

    return mappings


